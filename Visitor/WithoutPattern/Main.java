//Нарушение принципа открытости-закрытости (OCP):
//Если нужно добавить новые операции (например, перевод стоимости в евро),
// придётся изменить классы Painting и Sculpture, что нарушает OCP.
//
//Жёсткая связанность:
//Классы экспонатов сами выполняют операции, такие как фото и оценка стоимости, что затрудняет расширение логики.
//
//Трудности в добавлении новых операций:
