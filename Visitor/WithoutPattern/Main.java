package BehavioralPatterns2.Visitor.WithoutPattern;

import java.util.List;

public class Main {
    public static void main(String[] args) {
        // Создаём список экспонатов
        List<Exhibit> exhibits = List.of(
                new Painting("Мона Лиза", 850000.00),
                new Sculpture("Давид", 950000.00)
        );

        // Выполняем операции для каждого экспоната
        for (Exhibit exhibit : exhibits) {
            System.out.println("Стоимость: " + exhibit.getPrice() + " USD");
            exhibit.takePhoto();
        }
    }
}

